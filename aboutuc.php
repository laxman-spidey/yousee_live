<h3>Join the Core Team</h3>

<p>UC welcomes members to join the UC Core team as fulltime Giving Ambassadors, either on entirely voluntary basis or with a very modest financial compensation. While some may take financial compensation for their work at UC, they volunteer to take a modest compensation that is primarily to help them meet their basic livelihood needs. Embodying this spirit in the core team is a strategic necessity for them to lead and develop the collaborative and voluntary platforms that UC aims to build for its work in the social development sector.</p>
<p>Here are some attributes we are looking for when someone chooses to join the UC core team.</p>
<ol>
<li>Strong inclination for social work</li>
<li>Open minded and quick at learning</li>
<li>Aptitude for field work and project execution</li>
<li>Willingness to constructively debate on ideas, issues and challenges</li>
<li>Good team worker and  communicator</li>
<li>Good documentation skills</li>
<li>Reasonably good analytical and quantitative skills</li>
<li>At ease with travel to different parts of the country for extended periods</li>
<li>Having some work experience would be helpful</li>
<li>Prepared for a modest compensation</li>
<li>Multitasker (a necessity given UC's multidimensional work and its early stage of growth)</li>
<li>Self Starter (should be able to plan by oneself without expecting micro level instructions and communicate well with team and partners to get tasks accomplished)</li>
</ol>

<p>UC is a giving platform, which invites resources in the form of volunteering time, waste donations and financial donations, to be deployed for result oriented social development work. UC team members would stand out as Ambassadors of Giving, through their own contribution of efforts for the activities undertaken by UC and also by taking a very modest compensation. UC encourages the team member to assess the minimum compensation that they would need to be independent, discharge their responsibilities and maintain good health. UC would finalise the compensation based on its budget feasibility and if the desired compensation is modest, reflecting the Giving spirit.</p>

