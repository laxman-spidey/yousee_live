<h3>Student Interns</h3>
<p>Student Interns are an integral part of UC team and have contributed with their skills and efforts to take UC's work forward. We welcome interns to work with UC with the following objectives:</p>
<ul>
<li>Opportunity to apply knowledge and skills to community service</li>
<li>Gain new skills and knowledge by doing onground work</li>
<li>Opportunity to contribute to positive social change</li>
</ul>	
<p>We recommend internship engagements to be not less than 3 weeks full-time participation in a project. Most projects at UC require real-time execution work, often requiring interns to multitask besides handling some core responsibilities. </p>
<p>Current opportunities range from coordinating, executing and documenting some of UC's and its partners projects. A range of skill sets from communication skills, project management, programming etc would be needed in such projects. Team Work, Empathy, Patience and Perseverance are key to UC's work, which is collaborative in nature.</p>
<p>Internships at UC are undertaken on a voluntary basis, integrating into the giving spirit that UC promotes. UC makes an effort to cover some of the travel and other incidental expenses that interns might incur as part the project work. Any such reimbursements covered are indicated by UC with respect to each project, based on its budgetary provisions. </p>
<p>Students who wish to explore internship projects with UC can contact UC by writing to gunaranjan@yousee.in </p>
