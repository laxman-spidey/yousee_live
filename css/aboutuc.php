<h1>UC Brand</h1>
<p>UC stands for the following ideas:</p>
<ol><li>The UC smiley logo is a combination of the letters U and C, and stands for the smiles, satisfaction and happiness that UC would strive to deliver to its stakeholders.</li>
<li>The "UC" put together stands for "YouSee" a hallmark of transparency and openness that the institution will uphold. "UC" & "YouSee" also stand for the organisation's commitment to show results in its work.</li>
<li>The U in the UC stands for "Unlimited & United", conveying the idea that intellectual, financial, and human resources are available in plenty to address the challenges faced by the poor and disadvantaged people, but we need to apply these Unlimited resources in an United and integrated manner to achieve results.</li>
<li> The C in UC stands for "Company & Care", indicating the institution's commitment to give Company and Care to the poor and disadvantaged, by improving thier opportunities for better livelihoods and enhancing quality of life.</li></ol>

<h2>UC Values</h2>
<p>The organization will nurture and stand for the following values</p>
<ol> 
<li>Openness and Transparency</li>
<li>Innovation</li>
<li>The four pillars house -4i- application of Integrity, Intensity and Intelligence, leading to Impact</li>
<li>Financial and Environmental Sustainability</li>
</ol>

<h2>UC Mission</h2>
<p>The organization will work to minimise risks, improve livelihood opportunities and quality of life, especially for the very poor and disadvantaged people. This would be done by making investments and developing and providing products & services along the following thematic areas:</p>
<ol>
<li>Education for creating</li>
	<ol>
		<li>Livelihoods and</li>
		<li>Capable, Concerned and Caring Citizens</li>
    </ol>
<li>Health for</li>
	<ol>
		<li>People and</li>
		<li>Planet (Energy and Environment related services)</li>
    </ol>
<li>Disaster relief services</li>
<li>Income generation activities and Old age security</li>
</ol>

<h2>UC Organisation</h2>
<p>UC is incorporated as "United Care Development Services" (UCDS), a not-for-profit company under section-25 of the Indian Companies Act 1956.</p>

